public class CalculatorObject {

    public  int addTwoInts(int a, int b){
        return a + b;
    }
    public double subtractTwoDoubles(double a, double b){
        return (a - b);
    }
    public double multiplyTwoDoubles(double a, double b){
        return (a * b);
    }
    public  int divideTwoInts(int a, int b){
        return (a / b);
    }
    public long divideTwoLongs(long a, long b){
        return (a / b);
    }
    public float addTwoFloatNumbers(float a, float b){
        return (a + b);
    }
    public float multiplyTwoFloats(float a, float b){
        return (a * b);
    }
    public float subtractTwoFloats(float a, float b){
        return (a - b);
    }
    public float divideTwoFloats(float a, float b) {
        return (a / b);
    }
}
